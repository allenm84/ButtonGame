		public ARGB(int rgb)
		{
			var h = new rgbHex{ value = rgb };
			A = 255;
			R = h.r;
			G = h.g;
			B = h.b;
		}

		public ARGB(byte a, byte r, byte g, byte b)
		{
			A = a;
			R = r;
			G = g;
			B = b;
		}

		public static ARGB FromArgb(byte alpha, ARGB rgb)
		{
			return new ARGB(alpha, rgb.R, rgb.G, rgb.B);
		}
